const { addSimBatch } = require('./sim-inventory');

module.exports = async (req, res) => {
    // Set CORS headers
    res.setHeader('Access-Control-Allow-Origin', '*');
    res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
    res.setHeader('Access-Control-Allow-Headers', 'Content-Type');

    if (req.method === 'OPTIONS') {
        return res.status(200).end();
    }

    if (req.method !== 'POST') {
        return res.status(405).json({ success: false, error: 'Method not allowed' });
    }

    try {
        const { iccids, batch_id } = req.body;

        if (!iccids) {
            return res.status(400).json({
                success: false,
                error: 'Missing iccids'
            });
        }

        // Split by newlines and filter empty lines
        const iccidArray = iccids.split('\n').filter(iccid => iccid.trim());

        const result = await addSimBatch(iccidArray, batch_id);

        return res.status(200).json(result);
    } catch (error) {
        console.error('Error adding SIM batch:', error);
        return res.status(500).json({
            success: false,
            error: error.message || 'Failed to add SIM batch'
        });
    }
};
